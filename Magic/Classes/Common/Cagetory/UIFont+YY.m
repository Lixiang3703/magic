#import "UIFont+YY.h"

@implementation UIFont (YY)

+ (UIFont *)YYFontHuge {
    return [UIFont systemFontOfSize:19];
}

+ (UIFont *)YYFontLarge {
    return [UIFont systemFontOfSize:16];
}

+ (UIFont *)YYFontBig {
    return [UIFont systemFontOfSize:14];
}

+ (UIFont *)YYFontNormal {
    return [UIFont systemFontOfSize:12];
}

+ (UIFont *)YYFontSmall {
    return [UIFont systemFontOfSize:10];
}

@end
